package br.com.leo.math;

public class SimpleMath {
	
	public Double sumOperation(Double num1, Double num2) {
		return num1 + num2;
	}
	
	public Double subOperation(Double num1, Double num2) {
		return num1 - num2;
	}
	
	public Double multOperation(Double num1, Double num2) {
		return num1 * num2;
	}

	public Double divOperation(Double num1, Double num2) {
		return num1 / num2;
	}
	
	public Double sqrtOperation(Double num1) {
		return Math.sqrt(num1);
	}
}
